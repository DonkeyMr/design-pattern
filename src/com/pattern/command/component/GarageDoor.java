package com.pattern.command.component;

public class GarageDoor {

    public void up() {
        System.out.println("GarageDoor is up");
    }

    public void down() {
        System.out.println("GarageDoor is down");
    }

    public void stop() {
        System.out.println("stop is stop");
    }

    public void lightOn() {
        System.out.println("GarageDoor is lightOn");
    }

    public void lightOff() {
        System.out.println("Garage is lightOff");
    }
}
